<?php

declare(strict_types=1);

namespace Sports\Tests\BaseballActionContactDetails;

use PHPUnit\Framework\TestCase;
use Sports\BaseballActionContactDetails\{ BaseballActionContactDetailsDto, BaseballActionContactDetailsModel, IBaseballActionContactDetailsService, BaseballActionContactDetailsService };

class BaseballActionContactDetailsServiceTest extends TestCase
{
    private $repository;
    private array $input;
    private BaseballActionContactDetailsDto $dto;
    private BaseballActionContactDetailsModel $model;
    private IBaseballActionContactDetailsService $service;

    protected function setUp(): void
    {
        $this->repository = $this->createMock("Sports\BaseballActionContactDetails\IBaseballActionContactDetailsRepository");
        $this->input = [
            "id" => 575,
            "baseball_action_pitch_id" => 3493,
            "location" => "world",
            "strength" => "nearly",
            "velocity" => 5164,
            "comment" => "So eat discuss year anything former prevent. Recent property least sport. Help fire marriage hold type rather claim.",
            "trajectory_coordinates" => "especially",
            "trajectory_formula" => "national",
        ];
        $this->dto = new BaseballActionContactDetailsDto($this->input);
        $this->model = new BaseballActionContactDetailsModel($this->dto);
        $this->service = new BaseballActionContactDetailsService($this->repository);
    }

    protected function tearDown(): void
    {
        unset($this->repository);
        unset($this->input);
        unset($this->dto);
        unset($this->model);
        unset($this->service);
    }

    public function testInsert_ReturnsId(): void
    {
        $expected = 1733;

        $this->repository->expects($this->once())
            ->method("insert")
            ->with($this->dto)
            ->willReturn($expected);

        $actual = $this->service->insert($this->model);
        $this->assertEquals($expected, $actual);
    }

    public function testInsert_ReturnsEmpty(): void
    {
        $expected = 0;

        $this->repository->expects($this->once())
            ->method("insert")
            ->with($this->dto)
            ->willReturn($expected);

        $actual = $this->service->insert($this->model);
        $this->assertEmpty($actual);
    }

    public function testUpdate_ReturnsRowCount(): void
    {
        $expected = 4181;

        $this->repository->expects($this->once())
            ->method("update")
            ->with($this->dto)
            ->willReturn($expected);

        $actual = $this->service->update($this->model);
        $this->assertEquals($expected, $actual);
    }

    public function testUpdate_ReturnsEmpty(): void
    {
        $expected = 0;

        $this->repository->expects($this->once())
            ->method("update")
            ->with($this->dto)
            ->willReturn($expected);

        $actual = $this->service->update($this->model);
        $this->assertEmpty($actual);
    }

    public function testGet_ReturnsNull(): void
    {
        $id = 1488;

        $this->repository->expects($this->once())
            ->method("get")
            ->with($id)
            ->willReturn(null);

        $actual = $this->service->get($id);
        $this->assertNull($actual);
    }

    public function testGet_ReturnsModel(): void
    {
        $id = 3117;

        $this->repository->expects($this->once())
            ->method("get")
            ->with($id)
            ->willReturn($this->dto);

        $actual = $this->service->get($id);
        $this->assertEquals($this->model, $actual);
    }

    public function testGetAll_ReturnsEmpty(): void
    {
        $this->repository->expects($this->once())
            ->method("getAll")
            ->willReturn([]);

        $actual = $this->service->getAll();
        $this->assertEmpty($actual);
    }

    public function testGetAll_ReturnsModels(): void
    {
        $this->repository->expects($this->once())
            ->method("getAll")
            ->willReturn([$this->dto]);

        $actual = $this->service->getAll();
        $this->assertEquals([$this->model], $actual);
    }

    public function testDelete_ReturnsRowCount(): void
    {
        $id = 3979;
        $expected = 5291;

        $this->repository->expects($this->once())
            ->method("delete")
            ->with($id)
            ->willReturn($expected);

        $actual = $this->service->delete($id);
        $this->assertEquals($expected, $actual);
    }

    public function testCreateModel_ReturnsNullIfEmpty(): void
    {
        $actual = $this->service->createModel([]);
        $this->assertNull($actual);
    }

    public function testCreateModel_ReturnsModel(): void
    {
        $actual = $this->service->createModel($this->input);
        $this->assertEquals($this->model, $actual);
    }
}