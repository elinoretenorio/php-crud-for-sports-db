<?php

declare(strict_types=1);

namespace Sports\Tests\BaseballDefensiveStats;

use PHPUnit\Framework\TestCase;
use Sports\BaseballDefensiveStats\{ BaseballDefensiveStatsDto, BaseballDefensiveStatsModel, IBaseballDefensiveStatsService, BaseballDefensiveStatsService };

class BaseballDefensiveStatsServiceTest extends TestCase
{
    private $repository;
    private array $input;
    private BaseballDefensiveStatsDto $dto;
    private BaseballDefensiveStatsModel $model;
    private IBaseballDefensiveStatsService $service;

    protected function setUp(): void
    {
        $this->repository = $this->createMock("Sports\BaseballDefensiveStats\IBaseballDefensiveStatsRepository");
        $this->input = [
            "id" => 442,
            "double_plays" => 4122,
            "triple_plays" => 8277,
            "putouts" => 6641,
            "assists" => 2110,
            "errors" => 9318,
            "fielding_percentage" => 993.71,
            "defensive_average" => 91.36,
            "errors_passed_ball" => 4488,
            "errors_catchers_interference" => 132,
        ];
        $this->dto = new BaseballDefensiveStatsDto($this->input);
        $this->model = new BaseballDefensiveStatsModel($this->dto);
        $this->service = new BaseballDefensiveStatsService($this->repository);
    }

    protected function tearDown(): void
    {
        unset($this->repository);
        unset($this->input);
        unset($this->dto);
        unset($this->model);
        unset($this->service);
    }

    public function testInsert_ReturnsId(): void
    {
        $expected = 2915;

        $this->repository->expects($this->once())
            ->method("insert")
            ->with($this->dto)
            ->willReturn($expected);

        $actual = $this->service->insert($this->model);
        $this->assertEquals($expected, $actual);
    }

    public function testInsert_ReturnsEmpty(): void
    {
        $expected = 0;

        $this->repository->expects($this->once())
            ->method("insert")
            ->with($this->dto)
            ->willReturn($expected);

        $actual = $this->service->insert($this->model);
        $this->assertEmpty($actual);
    }

    public function testUpdate_ReturnsRowCount(): void
    {
        $expected = 2288;

        $this->repository->expects($this->once())
            ->method("update")
            ->with($this->dto)
            ->willReturn($expected);

        $actual = $this->service->update($this->model);
        $this->assertEquals($expected, $actual);
    }

    public function testUpdate_ReturnsEmpty(): void
    {
        $expected = 0;

        $this->repository->expects($this->once())
            ->method("update")
            ->with($this->dto)
            ->willReturn($expected);

        $actual = $this->service->update($this->model);
        $this->assertEmpty($actual);
    }

    public function testGet_ReturnsNull(): void
    {
        $id = 4810;

        $this->repository->expects($this->once())
            ->method("get")
            ->with($id)
            ->willReturn(null);

        $actual = $this->service->get($id);
        $this->assertNull($actual);
    }

    public function testGet_ReturnsModel(): void
    {
        $id = 7720;

        $this->repository->expects($this->once())
            ->method("get")
            ->with($id)
            ->willReturn($this->dto);

        $actual = $this->service->get($id);
        $this->assertEquals($this->model, $actual);
    }

    public function testGetAll_ReturnsEmpty(): void
    {
        $this->repository->expects($this->once())
            ->method("getAll")
            ->willReturn([]);

        $actual = $this->service->getAll();
        $this->assertEmpty($actual);
    }

    public function testGetAll_ReturnsModels(): void
    {
        $this->repository->expects($this->once())
            ->method("getAll")
            ->willReturn([$this->dto]);

        $actual = $this->service->getAll();
        $this->assertEquals([$this->model], $actual);
    }

    public function testDelete_ReturnsRowCount(): void
    {
        $id = 2054;
        $expected = 9186;

        $this->repository->expects($this->once())
            ->method("delete")
            ->with($id)
            ->willReturn($expected);

        $actual = $this->service->delete($id);
        $this->assertEquals($expected, $actual);
    }

    public function testCreateModel_ReturnsNullIfEmpty(): void
    {
        $actual = $this->service->createModel([]);
        $this->assertNull($actual);
    }

    public function testCreateModel_ReturnsModel(): void
    {
        $actual = $this->service->createModel($this->input);
        $this->assertEquals($this->model, $actual);
    }
}