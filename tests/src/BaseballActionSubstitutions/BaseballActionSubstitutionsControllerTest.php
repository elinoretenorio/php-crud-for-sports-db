<?php

declare(strict_types=1);

namespace Sports\Tests\BaseballActionSubstitutions;

use PHPUnit\Framework\TestCase;
use Sports\BaseballActionSubstitutions\{ BaseballActionSubstitutionsDto, BaseballActionSubstitutionsModel, BaseballActionSubstitutionsController };

class BaseballActionSubstitutionsControllerTest extends TestCase
{
    private array $input;
    private BaseballActionSubstitutionsDto $dto;
    private BaseballActionSubstitutionsModel $model;
    private $service;
    private $request;
    private $stream;
    private BaseballActionSubstitutionsController $controller;

    protected function setUp(): void
    {
        $this->input = [
            "id" => 786,
            "baseball_event_state_id" => 1626,
            "sequence_number" => 2801,
            "person_type" => "shoulder",
            "person_original_id" => 462,
            "person_original_position_id" => 802,
            "person_original_lineup_slot" => 1670,
            "person_replacing_id" => 3149,
            "person_replacing_position_id" => 7553,
            "person_replacing_lineup_slot" => 857,
            "substitution_reason" => "suddenly",
            "comment" => "thing",
        ];
        $this->dto = new BaseballActionSubstitutionsDto($this->input);
        $this->model = new BaseballActionSubstitutionsModel($this->dto);
        $this->service = $this->createMock("Sports\BaseballActionSubstitutions\IBaseballActionSubstitutionsService");
        $this->request = $this->createMock("Psr\Http\Message\ServerRequestInterface");
        $this->stream = $this->createMock("Psr\Http\Message\StreamInterface");
        $this->controller = new BaseballActionSubstitutionsController(
            $this->service
        );

        $this->stream->method("getContents")
            ->willReturn("[]");

        $this->request->method("getBody")
            ->willReturn($this->stream);

        $this->request->method("getParsedBody")
            ->willReturn($this->input);
    }

    protected function tearDown(): void
    {
        unset($this->input);
        unset($this->dto);
        unset($this->model);
        unset($this->service);
        unset($this->request);
        unset($this->stream);
        unset($this->controller);
    }

    public function testInsert_ReturnsResponse(): void
    {
        $id = 1190;
        $expected = ["result" => $id];
        $args = [];

        $this->service->expects($this->once())
            ->method("createModel")
            ->with($this->request->getParsedBody())
            ->willReturn($this->model);
        $this->service->expects($this->once())
            ->method("insert")
            ->with($this->model)
            ->willReturn($id);

        $actual = $this->controller->insert($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testUpdate_ReturnsErrorResponse(): void
    {
        $expected = ["result" => 0, "message" => "Invalid input"];
        $args = ["id" => 0];

        $actual = $this->controller->update($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testUpdate_ReturnsResponse(): void
    {
        $id = 3183;
        $expected = ["result" => $id];
        $args = ["id" => 6272];

        $this->service->expects($this->once())
            ->method("createModel")
            ->with($this->request->getParsedBody())
            ->willReturn($this->model);
        $this->service->expects($this->once())
            ->method("update")
            ->with($this->model)
            ->willReturn($id);

        $actual = $this->controller->update($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testGet_ReturnsErrorResponse(): void
    {
        $expected = ["result" => 0, "message" => "Invalid input"];
        $args = ["id" => 0];

        $actual = $this->controller->get($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testGet_ReturnsResponse(): void
    {
        $expected = ["result" => $this->model->jsonSerialize()];
        $args = ["id" => 2375];

        $this->service->expects($this->once())
            ->method("get")
            ->with($args["id"])
            ->willReturn($this->model);

        $actual = $this->controller->get($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testGetAll_ReturnsResponse(): void
    {
        $expected = ["result" => [$this->model->jsonSerialize()]];
        $args = [];

        $this->service->expects($this->once())
            ->method("getAll")
            ->willReturn([$this->model]);

        $actual = $this->controller->getAll($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testDelete_ReturnsErrorResponse(): void
    {
        $expected = ["result" => 0, "message" => "Invalid input"];
        $args = ["id" => 0];

        $actual = $this->controller->delete($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }

    public function testDelete_ReturnsResponse(): void
    {
        $id = 9111;
        $expected = ["result" => $id];
        $args = ["id" => 7070];

        $this->service->expects($this->once())
            ->method("delete")
            ->with($args["id"])
            ->willReturn($id);

        $actual = $this->controller->delete($this->request, $args);
        $this->assertEquals($expected, $actual->getPayload());
    }
}